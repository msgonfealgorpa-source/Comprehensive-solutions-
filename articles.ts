export function getArticleBySlug(slug: string): Article | null {
  return ARTICLES_DATABASE[slug] || null;
}

export function getAllArticles(): ArticleListItem[] {
  return Object.values(ARTICLES_DATABASE).map(article => ({
    slug: article.slug,
    title: article.title,
    description: article.description,
    category: article.category,
    categoryName: article.categoryName,
    keyword: article.keyword
  }));
}

export function getArticlesByCategory(category: string): ArticleListItem[] {
  return Object.values(ARTICLES_DATABASE)
    .filter(article => article.category === category)
    .map(article => ({
      slug: article.slug,
      title: article.title,
      description: article.description,
      category: article.category,
      categoryName: article.categoryName,
      keyword: article.keyword
    }));
}

export function getCategories(): { slug: string; name: string; count: number }[] {
  const categoryMap = new Map<string, { name: string; count: number }>();
  
  Object.values(ARTICLES_DATABASE).forEach(article => {
    const existing = categoryMap.get(article.category);
    if (existing) {
      categoryMap.set(article.category, { name: article.categoryName, count: existing.count + 1 });
    } else {
      categoryMap.set(article.category, { name: article.categoryName, count: 1 });
    }
  });
  
  return Array.from(categoryMap.entries()).map(([slug, data]) => ({
    slug,
    name: data.name,
    count: data.count
  }));
}

export function getRelatedArticles(currentSlug: string, category: string, limit: number = 4): ArticleListItem[] {
  return Object.values(ARTICLES_DATABASE)
    .filter(article => article.category === category && article.slug !== currentSlug)
    .slice(0, limit)
    .map(article => ({
      slug: article.slug,
      title: article.title,
      description: article.description,
      category: article.category,
      categoryName: article.categoryName,
      keyword: article.keyword
    }));
}

export function getAllSlugs(): string[] {
  return Object.keys(ARTICLES_DATABASE);
}

