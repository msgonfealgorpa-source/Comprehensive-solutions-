export function generateFAQSchema(faq: FAQItem[]): object {
  return {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    mainEntity: faq.map(item => ({
      '@type': 'Question',
      name: item.question,
      acceptedAnswer: {
        '@type': 'Answer',
        text: item.answer
      }
    }))
  };
}

export function generateArticleSchema(article: Article): object {
  return {
    '@context': 'https://schema.org',
    '@type': 'Article',
    headline: article.title,
    description: article.description,
    author: {
      '@type': 'Organization',
      name: 'حلول تقنية'
    },
    publisher: {
      '@type': 'Organization',
      name: 'حلول تقنية'
    },
    datePublished: article.publishedAt,
    dateModified: article.publishedAt,
    mainEntityOfPage: {
      '@type': 'WebPage',
      '@id': `/${article.slug}`
    }
  };
}

export function generateBreadcrumbSchema(article: Article): object {
  return {
    '@context': 'https://schema.org',
    '@type': 'BreadcrumbList',
    itemListElement: [
      {
        '@type': 'ListItem',
        position: 1,
        name: 'الرئيسية',
        item: '/'
      },
      {
        '@type': 'ListItem',
        position: 2,
        name: article.categoryName,
        item: `/category/${article.category}`
      },
      {
        '@type': 'ListItem',
        position: 3,
        name: article.keyword,
        item: `/${article.slug}`
      }
    ]
  };
}
